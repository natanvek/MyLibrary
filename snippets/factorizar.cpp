vpii factorizar(ll x) {
    vpii res = {};

    if(x % 2 == 0) {
        res.pb({2, 0});
        while(x % 2 == 0) {
            res.back().second++;
            x /= 2;
        }
    }

    for(ll k = 3; k * k <= x; k += 2){
        if(x % k) continue;
        res.pb({k, 0});
        while(x % k == 0) {
            res.back().second++;
            x /= k;
        }
    }
    if(x > 1) res.pb({x, 1}); 
    return res;
}
